# runme.R ----
# Bootstrap script: run once after cloning to check dependencies and folder
# structure, then build the pipeline and render the manuscript.
#
# Does NOT scaffold a new Quarto project - manuscript.qmd, _quarto.yml, etc.
# are already part of this template and version-controlled. Re-running
# quarto::quarto_create_project() here would overwrite your customised
# YAML/content, so don't.

# 1. Check required packages ----
required_pkgs <- c(
  "targets",     # pipeline
  "tarchetypes", # tar_quarto() and friends
  "quarto",      # render + project helpers
  "tibble",      # used by the example target — swap/extend as needed
  "here",        # anchors file paths to the project root everywhere
  "jsonlite",    # used by detect_render_format() (R/functions.R)
  # Used by supplementary/tables-mre.qmd - drop if you delete that notebook.
  "kableExtra",
  "gt",
  "flextable",
  "huxtable",
  "DT",
  # Used by supplementary/images-mre.qmd and generate-images.R - drop if
  # you delete that notebook. Mermaid needs no extra R package (Quarto
  # handles it), but docx SVG embedding also needs a system `rsvg-convert`
  # (librsvg) on PATH - see the notebook for details.
  "ggplot2",
  "magick",
  "svglite",
  # Used by supplementary/plots-mre.qmd - drop if you delete that notebook.
  "ggtext",
  "patchwork",
  # Used by supplementary/fonts-mre.qmd and generate-fonts.R - drop if you
  # delete that notebook. systemfonts/ragg is the recommended combo;
  # showtext/sysfonts and extrafont are also loaded there for direct
  # comparison (extrafont specifically to demonstrate why it's a poor fit
  # here - see the notebook).
  "systemfonts",
  "ragg",
  "showtext",
  "sysfonts",
  "extrafont",
  # Used by tests/testthat/ - see manuscript/supplementary/testing-mre.qmd.
  "testthat",
  "withr"
)

missing_pkgs <- required_pkgs[
  !vapply(required_pkgs, requireNamespace, logical(1), quietly = TRUE)
]

if (length(missing_pkgs) > 0) {
  message("Installing missing packages: ", paste(missing_pkgs, collapse = ", "))
  install.packages(missing_pkgs)
}

# 2. Ensure folder structure exists ----
# R/                 - functions, sourced by _targets.R via tar_source()
# manuscript/         - self-contained Quarto project: manuscript.qmd, its
#                       own _quarto.yml, tables/, figures/, supplementary/,
#                       styles/, references.bib (tracked in git) - see
#                       "Directory layout" in CLAUDE.md/README.md
# data-raw/           - raw, as-received data + import/cleaning scripts (tracked)
# data/               - cleaned data produced by data-raw/import-data.R
#                       (git-ignored, not regenerated by tar_make() itself -
#                       see data-raw/data-raw-readme.md)
# manuscript/output/  - rendered manuscript (html/docx/typst), git-ignored,
#                       rebuilt by tar_make(). MUST stay inside manuscript/:
#                       output-dir outside its own project dir is a
#                       genuinely broken config, confirmed by testing
#                       (scattered output, a recurring typst "file not
#                       found" bug, a docx-corruption regression) - see
#                       CLAUDE.md's "Directory layout".
# submission/         - standalone per-table/per-figure exports for
#                       journals that need individually named files
#                       (tbl-01-*.docx, fig-01-*.tif, ...), git-ignored,
#                       rebuilt by tar_make(). Deliberately NOT inside
#                       manuscript/output/ - Quarto's website render
#                       deletes anything under its own output-dir it
#                       doesn't recognize as its own.
required_dirs <- c(
  "R",
  "manuscript", "manuscript/tables", "manuscript/figures",
  "manuscript/supplementary", "manuscript/styles",
  "data-raw", "data", "manuscript/output", "submission"
)

for (d in required_dirs) {
  if (!dir.exists(d)) {
    dir.create(d, recursive = TRUE)
    message("Created missing directory: ", d)
  }
}

# 3. Import the example raw data, if not already done ----
# data/example-data.rds is what the pipeline's import_data target reads
# (_targets.R); it comes from data-raw/import-data.R, a one-time script
# per the usethis::use_data_raw() convention (see data-raw/data-raw-readme.md)
# - not rerun on every tar_make(), only here on first setup and whenever
# you rerun it by hand after editing the raw file.
if (!file.exists("data/example-data.rds")) {
  source("data-raw/import-data.R")
}

# 4. Sanity-check the Quarto install ----
# Fails loudly and early if Quarto CLI isn't on PATH, rather than deep into tar_make().
quarto::quarto_version()

# 5. Run the targets pipeline ----
# Builds data/model/figure targets and renders manuscript.qmd (tar_quarto() target).
targets::tar_make()

# 6. Inspect pipeline status ----
# Confirms what built, with timings/dependencies.
targets::tar_visnetwork()
